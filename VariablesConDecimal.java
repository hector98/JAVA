/**
*
*Uso de Variables con punto decimal
*
*/

public class VariablesConDecimal
{
 public static void main(String[] args)
 {
   //Se delcaran las varibales x, y como doubles
   double x, y;
   
   //Se les asignan valores
   x=3.14;
   y=9.92748372;
   
   System.out.println("El valor de x es: "+x);
   System.out.println("El valor de y es: "+y);
 }
}
